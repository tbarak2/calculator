# operations.py
class Operations:
    @staticmethod
    def add(operand1, operand2):
        return operand1 + operand2

    @staticmethod
    def subtract(operand1, operand2):
        return operand1 - operand2

    @staticmethod
    def multiply(operand1, operand2):
        return operand1 * operand2

    @staticmethod
    def divide(operand1, operand2):
        return operand1 / operand2
